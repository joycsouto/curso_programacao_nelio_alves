public class ExercicioTrapezio {
    public static void main(String[] args) {
        double h, b, B, area;
        b = 6.0;
        B = 8.0;
        h = 5.0;
        area = (b + B) / 2.0 * h;
        System.out.println(area);


        int a, c;
        double resultado;
        a = 5;
        c = 2;
        resultado = (double) a / c;
        System.out.println(resultado);


        double ab;
        int cd;

        ab = 5.0;
        cd = (int)ab;
        System.out.println(cd);

    }

}
